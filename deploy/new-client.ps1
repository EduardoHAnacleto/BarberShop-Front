Write-Host ''
Write-Host "Instance package for '$ShopName' generated at:" -ForegroundColor Green
Write-Host "  $OutputDir"
Write-Host ''
Write-Host 'Files:'
Get-ChildItem $OutputDir -Name | ForEach-Object { Write-Host "  - $_" }
