Write-Host "Domain:   https://$Domain"
Write-Host "Email:    delivery $(if ($emailEnabled -eq 'true') { "via $SmtpHost" } else { 'DISABLED (logged only — no SMTP given)' })"
Write-Host ''
Write-Host 'Next: follow README-DEPLOY.md in that folder.' -ForegroundColor Yellow
